<h4>Écarts de revenus entre dirigeants :</h4> 

<p> 

Les inégalités de revenus entre hommes et femmes sont significatives, quel que soit le statut juridique de l'entreprise. Par exemple, parmi les 	auto-entrepreneurs, les femmes gagnent en moyenne 20 % de moins que les hommes. Cet écart s'élève à 25 % chez les entrepreneurs 		individuels "classiques" et à 29 % chez les gérants de sociétés à responsabilité limitée (SARL). Ces différences de revenus peuvent limiter la 	capacité des femmes à investir dans leur entreprise ou à obtenir des financements externes. 

</p> 

