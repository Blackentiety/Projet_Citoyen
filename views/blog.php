  <h3>II. Obstacles et Défis Différenciés</h2> 

<h4>1- L'accès au financement :</h3> 

<h5>Le refus de Prêts Bancaires</h4> 

 

<p> 

Selon une étude menée auprès de 4000 PME américaines (éléments rapportés par le site Entrepreneure ambitieuse), les conditions des prêts 	sont plus favorables aux hommes qu'aux femmes. Dans les faits, plus de la moitié d'entre elles se voient refuser un prêt bancaire (deux fois 	plus souvent que les hommes). Une discrimination d'accès au financement qui constitue un réel handicap économique pour l'entrepreneuriat 	féminin.  

</p> 

 

<h4>Principal Obstacle</h4> 

 

<p> 

Pour 51% des créatrices, l'accès au financement a été le principal frein dans la mise en place de leur projet selon l'étude de notre partenaire, 	l'Adie. Les femmes présentent des plans de financement plus modestes et les montants qui leur sont prêtés sont inférieurs de 33% à ceux 	accordés aux hommes. 25% des femmes estiment qu'elles ont sous-évalué leur demande et 26% considèrent avoir obtenu un prêt inférieur à 	leur souhait. 

</p> 

 

<h4>Montants accordés</h4> 

 

<p> 

Les femmes entrepreneures ont tendance à solliciter des financements de montants inférieurs à ceux demandés par les hommes ; En 			conséquence, les prêts qui leur sont accordés sont généralement inférieurs de 33% à ceux des hommes. Cette situation peut s'expliquer par 	une sous-évaluation de leurs besoins financiers ou par une perception de risque plus élevée de la part des prêteurs. 

</p> 

 

 

<h4>Statistique sur l’accès au financement :</h4> 

 

<p> 

Selon une étude de France Active, 39 % des femmes estiment que l'accès aux prêts bancaires est plus difficile pour elles, contre 25 % des 	hommes partageant ce sentiment. De plus, environ une femme sur cinq considère qu'elle ose moins solliciter des crédits et des financements 	bancaires que les hommes. 

</p> <br/> 

 

<p> 

Selon l'enquête Findex de 2017, les hommes ont 3 % de chances en plus d'emprunter et 6 % de chances en plus d'épargner, par rapport aux 	femmes, pour créer, gérer et développer une entreprise. 

</p> 

 

<h4>Pourquoi les hommes ont plus d'opportunités que les femmes pour l'accès au financement :</h4> 

<p> 

Les hommes bénéficient souvent de meilleures opportunités d'accès au financement que les femmes entrepreneures, en raison de divers 		facteurs systémiques et culturels. 

</p> 

 

<p>Les exemples : </br> 

 

<h4>Différences sectorielles et revenus associés :</h4> 

<p> 

Les hommes et les femmes ne sont pas également représentés dans tous les secteurs d'activité, ce qui influence leur accès au financement 	et leurs revenus. Par exemple, les hommes sont majoritaires dans des secteurs comme la construction, l'industrie et les transports, qui sont 	souvent associés à des revenus plus élevés. En revanche, les femmes sont plus présentes dans des secteurs tels que la santé et les services 	aux particuliers, où les marges bénéficiaires peuvent être plus faibles. Cette répartition sectorielle contribue aux écarts de revenus entre les 	sexes. 

</p> 

